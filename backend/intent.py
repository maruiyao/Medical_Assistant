@lru_cache(maxsize=1)
def _get_intent_model():
    if not API_KEY or not MODEL:
        return None
    return init_chat_model(
        model=MODEL,
        model_provider="openai",
        api_key=API_KEY,
        base_url=BASE_URL,
        temperature=0,
        stream_usage=True,
    )


def run_intent_recognition(query: str) -> str:
    """返回 LLM 自由文本，供 neo4j_queries 用子串（如「简介」「病因」）匹配分支。"""
    m = _get_intent_model()
    if m is None:
        return '["查询疾病简介"]  # 未配置 ARK_API_KEY / MODEL 时的回退'
    try:
        out = m.invoke([HumanMessage(content=INTENT_PROMPT.format(query=query))])
        return (out.content or "").strip()
    except Exception:
        return '["查询疾病简介"]  # 意图识别异常回退'
